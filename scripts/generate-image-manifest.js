#!/usr/bin/env node
/**
 * Scans src/assets/img for files matching "<prefix>-<n>.<ext>" and emits a
 * manifest of arrays sorted by n. Single-image files (no -n suffix) are not
 * included. The generated TS file is consumed by the projects page so adding
 * new screenshots requires no code changes — just drop the file in and rebuild.
 */
const fs = require('fs');
const path = require('path');

const IMG_DIR = path.join(__dirname, '..', 'src', 'assets', 'img');
const OUT = path.join(__dirname, '..', 'src', 'app', 'pages', 'projects', 'image-manifest.ts');

const EXT_RE = /\.(jpe?g|png|webp|gif|avif)$/i;
const NUMBERED_RE = /^(.+)-(\d+)\.(jpe?g|png|webp|gif|avif)$/i;

const groups = {};
for (const file of fs.readdirSync(IMG_DIR)) {
  if (!EXT_RE.test(file)) continue;
  const m = file.match(NUMBERED_RE);
  if (!m) continue;
  const [, prefix, num] = m;
  (groups[prefix] ||= []).push({ num: Number(num), file });
}

const manifest = {};
for (const prefix of Object.keys(groups).sort()) {
  groups[prefix].sort((a, b) => a.num - b.num);
  manifest[prefix] = groups[prefix].map(i => `assets/img/${i.file}`);
}

const header =
  '// AUTO-GENERATED by scripts/generate-image-manifest.js — do not edit by hand.\n' +
  '// Run `npm run generate:images` (or any npm script that depends on it) to refresh.\n\n';

const body =
  'export const IMAGE_MANIFEST: Record<string, readonly string[]> = ' +
  JSON.stringify(manifest, null, 2) +
  ' as const;\n';

fs.writeFileSync(OUT, header + body);
console.log(`Wrote ${path.relative(process.cwd(), OUT)} with ${Object.keys(manifest).length} image groups.`);
